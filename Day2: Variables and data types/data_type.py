# String
var1 = "Hello world"
print(type(var1))

# Integer
var2 = 1
print(type(var2))

# float
var3 = 1.23
print(type(var3))

# bool
var4 = False
print(type(var4))

# Complex
var5 = complex(2, 4)
print(type(var5))


# List
all_values = [1, 1.2, False, True]
print(type(all_values))

# Tuple
t = (1, 3, 4, 5, 6)
print(type(t))

# Mapped data: Dictionary
mapped = {
    "Shishir": 0,
    "Yash": 1
}

print(type(mapped))

# type() function is used to check the type of datatype
